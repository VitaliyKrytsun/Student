# Конвертер секунд в години:хвилини:секунди
x = int(input('Введіть кількість секунд :'))
h = (x//3600)%24 # Визначення кількості годин
m = (x//60)%60 # ___ хвилин
s = x%60 # ___ секунд
if m<10: # Для красоти, щоб було двох значне число
    m=str('0'+str(m)) 
if s<10:
    s=str('0'+str(s))
print('Час: '+str(h)+':'+str(m)+':'+str(s))